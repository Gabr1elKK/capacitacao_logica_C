/*
    Criação de variáveis e operações aritméticas.
*/

#include <stdio.h>

int main() {
    int a = 10;
    int b = 3;

    printf("Adição: %d\n", a + b);
    printf("Subtração: %d\n", a - b);
    printf("Multiplicação: %d\n", a * b);
    printf("Divisão: %d\n", a / b);
    printf("Módulo: %d\n", a % b); //resto da divisão

    return 0;
}